public class DefaultValue {
    static int  val1;
    static float  val2;
    static double   val3;
    static long  val4;
    static  boolean  val5;
    static char val6;

    public static void main(String[] args) {
        System.out.println("Default Values of data types ....");
        System.out.println("int = "+val1);
        System.out.println("Float = "+val2);
        System.out.println("Double = "+val3);
        System.out.println("Long = "+val4);
        System.out.println("Boolean = "+val5);
        System.out.println("Char = "+val6);

    }
}
